pub mod calculator {

    pub fn sum(x: i32, y: i32) -> i32 {
        let result: i32 = x + y;
        result
    }

    pub fn subtraction(x: i32, y: i32) -> i32 {
        let result: i32 = x - y;
        result
    }

    pub fn multiplication(x: i32, y: i32) -> i32 {
        let result: i32 = x * y;
        result
    }

    pub fn division(x: i32, y: i32) -> i32 {
        let result: i32 = x / y;
        result
    }
}
